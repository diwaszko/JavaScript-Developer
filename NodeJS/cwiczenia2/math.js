function adding(val1, val2){
    return  val1 + val2;
}
function subtraction(val1, val2){
    return  val1 - val2;
}
function muliplay(val1, val2){
    return  val1 * val2;
}
function divide(val1, val2){
    return  val1 / val2;
}

module.exports = {  
    adding: adding,      
    subtraction: subtraction,                            
    muliplay: muliplay,                     
    divide: divide,
}